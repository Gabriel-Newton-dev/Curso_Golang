// Como calcular a tabuada de multiplicação
// para os números de 1 a 9 em Golang - Exercícios Resolvidos de Java

package main

import "fmt"

func main() {

	fmt.Println("      Tabuada de Multiplicação    ")
	fmt.Println("----------------------------------------------")

	for i := 1; i <= 9; i++ {
		fmt.Printf("%d |\n", i)
	}

}
